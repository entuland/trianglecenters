SDL_Surface* GUI::Font::GetChar(std::string Char, RGBColor ForeCol,
                           RGBColor BackCol, int PixSize) {
    if ((badfont) || (Char.size()<1) || (PixSize>8) || (PixSize<1)) return 0;
    size_t x, y, i;
    Rect curpix;
    static SDL_Surface * curChar;
    SDL_FreeSurface(curChar);
    std::map<std::string, std::vector<size_t> >::iterator MapIter;
    MapIter = IntImage.find(Char);
    std::vector<size_t>* vec;
    if (MapIter == IntImage.end()) {
        const char single[2] = { 127, 0};
        vec = &IntImage[std::string(&single[0])];
    } else {
        vec = &IntImage[Char];
    }
    x = vec->at(0);
    y = vec->at(1);
    i = 1;

    if ((x*y+2) != vec->size()) return 0;

    curChar = SDL_CreateRGBSurface(SDL_HWSURFACE,
                                   x * PixSize, y * PixSize, 32,
                                   rmask, gmask, bmask, 0);

    Uint32 fgcol = Map(ForeCol, curChar);
    Uint32 bgcol = Map(BackCol, curChar);

    Uint32 curcol;


    for (size_t iy = 0; iy < y; ++iy) {
        for (size_t ix = 0; ix < x; ++ix) {
            curcol = (vec->at(++i) == 1) ? fgcol : bgcol;
            curpix.x = ix * PixSize;
            curpix.y = iy * PixSize;
            curpix.w = PixSize;
            curpix.h = PixSize;
            SDL_FillRect(curChar, &curpix, curcol);
        }
    }
    return curChar;
}

Uint16 GUI::RenderLine(std::string Line, SDL_Surface * Dest, Uint16 X, Uint16 Y,
                     Font& font, RGBColor ForeCol, RGBColor BackCol, int PixSize) {
    SDL_Surface* curchar;
    SDL_Rect curp;
    size_t slen = 0;
    int xoff = 0;
    slen = Line.size();
    if (slen<1) return 0;

    for (size_t ic=0; ic<slen; ++ic) {
        std::string CurS = Line.substr(ic,1);
        curchar = font.GetChar(CurS, ForeCol, BackCol, PixSize);
        if (CurS == "\n") {
            Y += curchar->h;
            xoff = 0;
        } else {
            curp.x = X + xoff;
            curp.y = Y;
            SDL_BlitSurface(curchar, 0, Dest, &curp);
            xoff += curchar->w;
        }
    }
    return Y + curchar->h;
}

int GUI::LineLen(std::string Line, Font& font, int PixSize) {

    int i = 0;
    size_t slen = 0;
    slen = Line.length();
    if (slen<1) return 0;
    SDL_Surface* curchar;
    RGBColor ForeCol, BackCol;

    for (size_t ic=0; ic<slen; ++ic) {
        curchar = font.GetChar(Line.substr(ic,1), ForeCol, BackCol, PixSize);
        i += curchar->w;
    }
    return i;
}

